export function searchUsersByName(query: string, limit = 20) {
  const needle = String(query || "")
    .trim()
    .toLowerCase();
  const rows = !needle
    ? DUMMY_USERS_RAW
    : DUMMY_USERS_RAW.filter((u: any) => {
        const fullName = `${u?.user?.prenom || ""} ${u?.user?.nom || ""}`
          .trim()
          .toLowerCase();
        const email = String(u?.user?.email || "").toLowerCase();
        const ecole = String(
          u?.ecole_name || u?.ecole?.abreviation || "",
        ).toLowerCase();
        return (
          fullName.includes(needle) ||
          email.includes(needle) ||
          ecole.includes(needle) ||
          String(u?.id || "").includes(needle)
        );
      });
  return rows.slice(0, limit);
}
